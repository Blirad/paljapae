/**
 * 운세 등급 + 오행 → 풀이 텍스트 3개 중 1개 반환
 * index 0~2 중 하나를 날짜 기반으로 선택
 */
export function getFortuneText(
  level: FortuneLevel,
  element: Element,
  variant: 0 | 1 | 2 = 0,
): string {
  const entry = FORTUNE_TEXTS.find(f => f.level === level && f.element === element)
  if (!entry) {
    return '오늘의 기운을 읽는 중입니다...'
  }
  return entry.texts[variant]
}

/** 날짜 기반 변형 인덱스 (0~2) */
export function getDailyVariant(year: number, month: number, day: number): 0 | 1 | 2 {
  return ((year + month + day) % 3) as 0 | 1 | 2
}
